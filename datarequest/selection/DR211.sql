    DISTINCT patientId AS '#patientId'
FROM
    datarequest
WHERE SUBSTRING_INDEX(doids, ',', 1) IN (0050872, 1324, 3907, 3908, 3910, 4872, 5409, 5421, 8007) OR
        SUBSTRING_INDEX(SUBSTRING_INDEX(doids, ',', 2),',',-1) IN (0050872, 1324, 3907, 3908, 3910, 4872, 5409, 5421, 8007) OR
        SUBSTRING_INDEX(SUBSTRING_INDEX(doids, ',', 3),',',-1) IN (0050872, 1324, 3907, 3908, 3910, 4872, 5409, 5421, 8007)
ORDER BY 1;